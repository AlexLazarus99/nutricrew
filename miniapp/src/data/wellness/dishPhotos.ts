export function dishPhotoCandidates(dishId: string): string[] {
  const ids = dishId === FALLBACK_DISH_ID ? [FALLBACK_DISH_ID] : [dishId, FALLBACK_DISH_ID];
  const urls: string[] = [];
  for (const id of ids) {
    urls.push(`${DISH_PHOTO_DIR}/${id}.jpg`);
    urls.push(`${DISH_PHOTO_DIR}/${id}.svg`);
  }
  return urls;
}

export function dishPhotoUrl(dishId: string): string {
  return `${DISH_PHOTO_DIR}/${dishId}.jpg`;
}

export function dishPhotoFallbackUrl(): string {
  return `${DISH_PHOTO_DIR}/${FALLBACK_DISH_ID}.jpg`;
}
